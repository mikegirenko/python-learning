age = 4
if age == 1:
    print('At ' + str(age) + ' years old person is a baby')
if age >= 2 and age < 4:
    print('At ' + str(age) + ' years old person is a toddler')
if age >= 4 and age < 13:
    print('At ' + str(age) + ' years old person is a kid')
if age >= 13 and age < 20:
    print('At ' + str(age) + ' years old person is a teenager')
if age >= 20 and age < 65:
    print('At ' + str(age) + ' years old person is an adult')
if age >= 65:
    print('At ' + str(age) + ' years old person is an elder')
